export function isEqual(p1, p2) {
  if (!p1 || !p2) {
    return false;
  }

  return p1[0] === p2[0] && p1[1] === p2[1];
}

export function sleep(ms) {
  return new Promise(resolve => {
    setTimeout(resolve, ms);
  });
}

export function generateRandomMaze(len) {
  const field = new Array(len).fill(null).map(() => {
    return new Array(len).fill(0).map(() => {
      return Math.random() < 0.35 ? 0 : 1;
    });
  });

  field[0][0] = 1;
  field[len - 1][len - 1] = 1;

  return {
    field,
    start: [0, 0],
    end: [len - 1, len - 1],
  };
}

